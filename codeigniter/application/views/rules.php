			<div>
				Pandemic is a cooperative game, meaning that rather than competing with each other, players will be working together as a team to try to stop the deadly pandemic from spreading across the world map. Here is a quick guide to getting started.
			</div>
			<div>
				<p class="topic">Players:</p>
				<p>2 to 4</p>
			</div>
			<div>
				<p class="topic"> Components:</p>
				96 wooden disease cubes, 5 player pawns, 6 wooden research stations, 6 markers (4 cure, 1 outbreak, 1 infection), 115 Cards (48 infection cards, 59 player cards, 4 role cards, 4 quick reference cards), and one board depicting a map of the world with connections between cities.
			</div>
			<div>
				<p class="topic"> Goal:</p>
				To cure all four diseases, before they infect the world.
			</div>
			<div>
				<p class="topic">Setup:</p>
				<p>Each player takes a random role card, a reference sheet, and a starting hand of cards (four each for 2 players, three for 3 players, two for 4 players). Infection and outbreak markers are placed at the first space on their respective tracks. A research station is placed in Atlanta, and players each put the pawn matching their role card in Atlanta.</p>
            </div></div>
                        <div id="container2">

				<p>Shuffle the infection deck. Draw three cards, and put three cubes of the appropriate color in each city. Draw three more cards, and put two cubes in each of those cities. Draw three more cards, and put one cube in each of those cities. All cards, once drawn, are placed in the infection discard pile.</p>

				<p>Divide the player deck into four equal piles, and shuffle one Epidemic card into each pile. Then place the four piles on top of each other to form the player deck.</p>

            <p>Once the player deck is ready, the player who has most recently been sick takes the first turn.</p>
               
				<p class="topic">How to Play:</p>
		<p> You may take up to four actions on your turn. Options for an action include:</p>

		<p>&#9762 <b>Move</b> to an adjacent city.</p>
		<p>&#9762 <b>Discard a card</b> to move to the named city.</p>
		<p>&#9762 <b>Discard a card</b> matching your current city to move to any city.</p>

		<p>&#9762 <b>Move</b> from your current city with a research station to any other city with a research station.</p>
		<p>&#9762 <b>Discard a card </b>matching your current city to build a research station there.</p>
		<p>&#9762 <b>Cure and remove</b> one disease cube from your current city.</p>
		<p>&#9762 <b>Pass a card</b> matching your current city to another player in the same city.</p>
		<p>&#9762 <b>Discard</b> five cards of the same color at a research station to cure that color disease.</p>
		<p>&#9762 Certain role cards will improve one or more of the above action choices.</p>

	<p>After your four actions, you will draw two cards from the player deck. Most of these are city cards, but some are Special Event cards which may be played at any time. Note that the hand limit of seven cards is strictly enforced at all times, so after increasing your hand beyond seven, you must play or discard cards until your hand size is down to seven.</p>

	<p>If you draw an Epidemic card, you must immediately discard the card, advance the infection rate marker, draw the bottom card of the infection deck and place three cubes in that city, and then shuffle the infection discard pile and place those cards on top of the infection deck.</p>

	<p>After drawing the player cards and resolving any epidemics, you must draw a number of infection cards equal to the current infection rate, adding one cube to each city revealed.</p>


	<p>If a fourth disease cube would ever be added to a city, that city suffers an outbreak, spreading cubes to all adjacent cities.</p>

	<p>If the player draw deck runs out, or the eighth outbreak happens, or if the cubes of any color run out, the players all lose the game. If the players manage to cure all four diseases before this happens, they win the game and rejoice in their shared victory.</p>
